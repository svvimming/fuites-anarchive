export const useGeneralStore = defineStore('general', () => {
  // ===================================================================== state
  const config = useRuntimeConfig()

  // ================================================================== computed
  const baseUrl = computed(() => config.public.backendUrl)

  // =================================================================== actions

  // ==================================================================== return
  return {
    // ----- state
    baseUrl
    // ----- actions
  }
})

if (import.meta.hot) {
  import.meta.hot.accept(acceptHMRUpdate(useGeneralStore, import.meta.hot))
}
